    echo "1) –ù–æ–≤—ã–π —Å–µ—Ä–≤–µ—Ä (ssh1.vast.ai:30421) - –†–ï–ö–û–ú–ï–ù–î–£–ï–¢–°–Ø"
    echo "2) –°—Ç–∞—Ä—ã–π —Å–µ—Ä–≤–µ—Ä (109.198.107.223:48937)"
    echo -n "–í—ã–±–æ—Ä (1/2) [–ø–æ —É–º–æ–ª—á–∞–Ω–∏—é 1]: "
    read choice
    # –ï—Å–ª–∏ –ø—É—Å—Ç–æ–π –≤–≤–æ–¥ - –∏—Å–ø–æ–ª—å–∑—É–µ–º –Ω–æ–≤—ã–π —Å–µ—Ä–≤–µ—Ä
