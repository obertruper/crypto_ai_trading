    echo -e "${YELLOW}–î–ª—è –æ—Ç–∫–ª—é—á–µ–Ω–∏—è –∏—Å–ø–æ–ª—å–∑—É–π—Ç–µ: exit${NC}"
    echo ""
    exec $SSH_CMD
fi